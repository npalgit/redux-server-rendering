import {
  GraphQLObjectType as ObjectType,
  GraphQLString as StringType,
  GraphQLInt as IntType,
  GraphQLFloat as FloatType,
  GraphQLInputObjectType as InputObjectType,
} from 'graphql';

export const propertyIncludesAttributes = new ObjectType({
  name: 'propertyIncludesAttributes',
  fields: {
    name: { type: StringType },
    display_name: { type: StringType },
    slug: { type: StringType },
    property_type_id: { type: IntType },
    display_address: { type: StringType },
    full_address: { type: StringType },
    city: { type: StringType },
    state: { type: StringType },
    country: { type: StringType },
    time_zone: { type: StringType },
    contact_name: { type: StringType },
    email: { type: StringType },
    phone: { type: StringType },
    lng: { type: FloatType },
    lat: { type: FloatType },
    status: { type: IntType },
    featured: { type: IntType },
    starting_at: { type: IntType },
    min_duration: { type: IntType },
    cancellation_policy: { type: IntType },
    max_capacity: { type: IntType },
    image_url: { type: StringType },
    details: { type: new InputObjectType({
      name: 'propDetails',
      fields: {
        id: { type: IntType },
        property_id: { type: IntType },
        deckline: { type: StringType },
        description: { type: StringType },
        executive_summary: { type: StringType },
        logo_url: { type: StringType },
        privacy_url: { type: StringType },
        terms_url: { type: StringType },
        status: { type: IntType },
        created_at: { type: StringType },
        updated_at: { type: StringType },
        moderated_at: { type: StringType },
        moderated_by: { type: StringType },
        deleted_at: { type: StringType },
        room_commission_rate: { type: FloatType },
        amenity_commission_rate: { type: FloatType },
      },
    }) },
    city_id: { type: StringType },
  },
});
